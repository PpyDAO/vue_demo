import Vue from 'vue'
import App from './App'
import router from './router'

Vue.config.productionTip = false

// 2.1 导入 vue-resource
import VueResource from 'vue-resource'
// 2.2 安装 vue-resource
Vue.use(VueResource)
// 设置请求的根路径
